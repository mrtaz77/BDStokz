INSERT INTO "BACKUP STOCK" (SYMBOL, VALUE, PRICE, UPDATE_TIME, AVAILABLE_LOTS, CONTROLLER_ID)
SELECT
    SYMBOL,
    VALUE,
    PRICE,
    CURRENT_TIMESTAMP AS UPDATE_TIME,
    CASE ROUND(DBMS_RANDOM.VALUE(1, 3))
        WHEN 1 THEN 1000
        WHEN 2 THEN 2500
        ELSE 2000
    END AS AVAILABLE_LOTS,
    CASE ROUND(DBMS_RANDOM.VALUE(1, 8))
        WHEN 1 THEN 1020
        WHEN 2 THEN 1030
        WHEN 3 THEN 1050
        WHEN 4 THEN 1060
        WHEN 5 THEN 1040
        WHEN 6 THEN 1000
        WHEN 7 THEN 1010
    END AS CONTROLLER_ID
FROM STOCK;
